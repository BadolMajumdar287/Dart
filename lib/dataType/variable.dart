void main() {
  var x = 10;
  var y = 20;

  var z = x + y;
  print(z);
}

// Variable is used to store numeric values and refer to them later in the program.
// x = variable name
// 10 = value assigned to the variable
// var = keyword to declare a variable
// const = keyword to declare a constant variable
// final = keyword to declare a final variable
// int = data type to declare an integer variable
// dynamic = A variable declared as dynamic can hold values of any type, and its type can change at runtime.
