//DART IMPORTANT ARITHMETIC OPERATORS

void main() {
  int a = 100;
  int b = 50;

  print("Addition: ${a + b}"); // Addition Operator
  print("Subtraction: ${a - b}"); // Subtraction Operator
  print("Multiplication: ${a * b}"); // Multiplication Operator
  print("Division: ${a / b}"); // Division Operator
  print("Integer Division: ${a ~/ b}"); // Integer Division Operator
  print("Modulus: ${a % b}"); // Modulus Operator
  print("Divide: ${a / b}"); // Divide Operator
}
