//DART UNARY OPERATORS

void main() {
  // Unary Plus Operator // Increment Operator

  int a = 10;

  //Prefix

  int b = ++a;
  print(b);

  // Postfix

  int c = a++;

  print(c);

  // Unary Minus Operator // Decrement Operator

  int f = 20;

  //Prifix

  int d = --f;
  print(d);

  //Postfix
  int e = f--;

  print(e);
}
